package ciclos;

public class Ciclos1_MVA {
	
	public static void main(String[] args) {
		
		// Programa un algoritmo que realice la tabla de multiplicar del 12
		
		int res=0;
		System.out.println("PROGRAMA QUE IMPRIME LA TABLA DEL 12");
		for (int i = 0; i < 11; i++) {
			res= 12*i;
			System.out.println(i + " X 12 = "+res);
		}
		
	}

}
